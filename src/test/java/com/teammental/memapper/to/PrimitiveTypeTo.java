public class PrimitiveTypeTo {
  private int id;
  private short sid;
  private float fid;
  private long lid;
  private double did;
  private char cid;
  private boolean bid;
  private byte byid;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public short getSid() {
    return sid;
  }

  public void setSid(short sid) {
    this.sid = sid;
  }

  public float getFid() {
    return fid;
  }

  public void setFid(float fid) {
    this.fid = fid;
  }

  public long getLid() {
    return lid;
  }

  public void setLid(long lid) {
    this.lid = lid;
  }

  public double getDid() {
    return did;
  }

  public void setDid(double did) {
    this.did = did;
  }

  public char getCid() {
    return cid;
  }

  public void setCid(char cid) {
    this.cid = cid;
  }

  public boolean getBid() {
    return bid;
  }

  public void setBid(boolean bid) {
    this.bid = bid;
  }

  public byte getByid() {
    return byid;
  }

  public void setByid(byte byid) {
    this.byid = byid;
  }
}
