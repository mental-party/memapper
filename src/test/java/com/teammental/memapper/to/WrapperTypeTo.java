public class WrapperTypeTo {
  private Integer id;
  private Short sid;
  private Long lid;
  private Float fid;
  private Double did;
  private Character cid;
  private Boolean bid;
  private Byte byid;

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }

  public Short getSid() {
    return sid;
  }

  public void setSid(Short sid) {
    this.sid = sid;
  }

  public Long getLid() {
    return lid;
  }

  public void setLid(Long lid) {
    this.lid = lid;
  }

  public Float getFid() {
    return fid;
  }

  public void setFid(Float fid) {
    this.fid = fid;
  }

  public Double getDid() {
    return did;
  }

  public void setDid(Double did) {
    this.did = did;
  }

  public Character getCid() {
    return cid;
  }

  public void setCid(Character cid) {
    this.cid = cid;
  }

  public Boolean getBid() {
    return bid;
  }

  public void setBid(Boolean bid) {
    this.bid = bid;
  }

  public Byte getByid() {
    return byid;
  }

  public void setByid(Byte byid) {
    this.byid = byid;
  }
}
